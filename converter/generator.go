// GenerateOptions controls what the generator emits.
type GenerateOptions struct {
	PackageName  string // Go package name for the generated file
	OutputDir    string // directory to write generated files into
	TypesFile    string // filename for the types file (empty = skip)
	BindingFile  string // filename for the bindings file
	SkipTypes    bool   // do not generate types_gen.go (types already defined)
	ReceiverType string // Go type used as method receiver (typically "HP")
	BindingPkg   string // import path of the hand-written binding package
}

// DefaultOptions targets the havok/generated sub-package.
// Types live in havok/binding; generated code wraps binding.Binding via the HP type.
func DefaultOptions() GenerateOptions {
	return GenerateOptions{
		PackageName:  "generated",
		OutputDir:    "./havok/generated",
		TypesFile:    "types_gen.go",
		BindingFile:  "bindings_gen.go",
		SkipTypes:    true,
		ReceiverType: "HP",
		BindingPkg:   "github.com/gsw945/havok-go/havok/binding",
	}
}

// Generate runs the generator with the given schema and options.
func Generate(schema *Schema, opts GenerateOptions) error {
	if err := os.MkdirAll(opts.OutputDir, 0o755); err != nil {
		return fmt.Errorf("converter: mkdir %s: %w", opts.OutputDir, err)
	}

	if !opts.SkipTypes && opts.TypesFile != "" {
		if err := generateTypes(schema, opts); err != nil {
			return err
		}
	}
	if err := generateBindings(schema, opts); err != nil {
		return err
	}
	return nil
}

